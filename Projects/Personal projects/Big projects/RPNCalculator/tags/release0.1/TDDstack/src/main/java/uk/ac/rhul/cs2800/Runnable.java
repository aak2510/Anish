package uk.ac.rhul.cs2800;

/**
 * This is a functional interface which is used to notify observers in the CalcController class to
 * execute a method.
 *
 * @author anish
 *
 */
@FunctionalInterface
public interface Runnable {
  /**
   * Tells the observer to execute a method.
   */
  public void notifyObservers();
}
